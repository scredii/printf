#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*tmp;
	int		i;

	tmp = (char*)malloc(sizeof(*tmp) * ft_strlen(s1) + 1);
	if (tmp == NULL)
		return (NULL);
	i = 0;
	while (s1[i])
	{
		tmp[i] = s1[i];
		i++;
	}
	tmp[i] = '\0';
	return (tmp);
}
